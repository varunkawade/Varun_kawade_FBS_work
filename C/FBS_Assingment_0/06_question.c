// Write a C program to find the square and cube of a given number.

 #include<stdio.h>
 
 int main(){
    int a = 4;
    int square , cube;
    square = a*a;
    cube = a*a*a;
    printf("a = %d\n", a);
    printf("square:%d\n", square);
    printf("cube:%d\n", cube);


    return 0;
 }